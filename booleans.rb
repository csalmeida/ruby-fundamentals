# Booleans evalues to true or false:
x = true
y = false

# Logic Operators
z = 20
w = []

puts z == 77 # false
puts z < 77 # true
puts z > 77 # false
puts z >= 77 # true
puts z >= 77 # false
puts !z # false
puts z != 77 # true
puts z < 77 && z > 3 # true
puts z < 77 || z > 10 # true

# Useful methods:
puts z.nil?
puts z.between?(1, 5)
puts w.empty?
